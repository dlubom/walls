<p class="hd1"><img height=32 src="images/icon1.bmp" alt="program icon" width=32 border=0> <I>WallsMap</I> - GIS Viewer and Shapefile Editor</p>
            
<p class="hd3">Version 0.3 Build 2014-11-23</p>

<p>WallsMap is a tool for managing geographical data inventories, where text, images, and links to information are associated
with sets of coordinates. Like other Geographic Information System (GIS) viewers, the program treats each map document as a set
of overlapping data layers. Among its unusual features is the ability to open multiple maps, or layer sets, so that they can be
viewed together in a synchronized fashion as shown here:
</p>

<img alt="3 synchronized map views" title="3 synchronized map views" height="261" src="images/Intro2.jpg" width="781">

<p>The WallsMap setup contains
a preliminary GIS project named <i>Texas Public Caves</i>, one that's similar to the
karst database being maintained by the <a href="http://texasspeleologicalsurvey.org/">Texas Speleological Survey</a>.
This dataset might interest you if you would like to share data with the TSS,
or if you're developing a project where data of various kinds (reports, images, etc.)
are associated with point locations in Texas.</p>

<p><a href="WallsMap-TX_setup.exe"><strong>Download Setup</strong></a> (131 MB)
&nbsp;&nbsp;&nbsp;<a href="WallsMap_ReadMe.txt"><strong>Release Notes</strong></a>

<p><strong>Note:</strong> WallsMap now uses graphics technology available only in recent
versions of Microsoft Windows (Vista/7/8+). Windows XP is not supported.
</p>

<p>A PDF version of the help file is also available and will be updated with each new build:
<a href="WallsMap.pdf">WallsMap.pdf</a> (5.5 MB)</p>

<p><b>Sample Project Info:</b>
Upon finishing, the program's setup should open a sample project file named <i>Texas Public Caves.ntl</i>. At
the same location is an alternative project, <i>Texas Public Caves_DRG-DEM.ntl</i>, that has the links and settings
already set up for the DEM/DRG layers described in the help file under <i>Available Background Imagery</i>.
Those layers currently cover an area of 1177 7.5' USGS quadrangles. Before opening that project, you should
download and extract the corresponding files into a folder named <i>TSS_NTI</i> at the same level as <i>TSS_Data</i>.
</p>

<hr>
<p>This program and the contained example project is offered courtesy
of the Texas Speleological Survey. Once it is installed you can click on a link in the
About box to check if your build is current.</p>
